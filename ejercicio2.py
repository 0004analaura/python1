nombre_completo = input("Ingresa tu nombre completo: ")

# Convertir el nombre completo a minúsculas
nombre_completo_minusculas = nombre_completo.lower()

# Convertir el nombre completo a mayúsculas
nombre_completo_mayusculas = nombre_completo.upper()

# Convertir el nombre completo a una cadena con la primera letra del nombre y de los apellidos en mayúscula
nombre_completo_mayusculas_iniciales = nombre_completo.title()

# Imprimir el nombre completo en minúsculas
print("En minúsculas:", nombre_completo_minusculas)

# Imprimir el nombre completo en mayúsculas
print("En mayúsculas:", nombre_completo_mayusculas)

# Imprimir el nombre completo con la primera letra en mayúscula
print("Con la primera letra en mayúscula:", nombre_completo_mayusculas_iniciales)